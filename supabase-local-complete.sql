create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = timezone('utc', now());
  return new;
end;
$$;

create or replace function public.only_digits(input text)
returns text
language sql
immutable
as $$
  select regexp_replace(coalesce(input, ''), '\D', '', 'g');
$$;

create sequence if not exists public.order_number_seq start 1000;

create or replace function public.generate_order_number()
returns text
language plpgsql
as $$
declare
  seq_value bigint;
begin
  seq_value := nextval('public.order_number_seq');
  return 'GM-' || to_char(current_date, 'YYYYMMDD') || '-' || lpad(seq_value::text, 6, '0');
end;
$$;

