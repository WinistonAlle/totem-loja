create or replace function public.refresh_order_totals(p_order_id uuid)
returns void
language plpgsql
as $$
declare
  v_total_items integer := 0;
  v_total_cents integer := 0;
begin
  select
    coalesce(sum(coalesce(quantity, 0))::integer, 0),
    coalesce(sum(round(coalesce(quantity, 0) * coalesce(unit_price_cents, 0)))::integer, 0)
  into v_total_items, v_total_cents
  from public.order_items
  where order_id = p_order_id;

  update public.orders
     set total_items = v_total_items,
         total_cents = v_total_cents,
         total_value = v_total_cents::numeric / 100,
         updated_at = timezone('utc', now())
   where id = p_order_id;
end;
$$;

create or replace function public.order_items_after_change()
returns trigger
language plpgsql
as $$
declare
  v_order_id uuid;
begin
  v_order_id := coalesce(new.order_id, old.order_id);
  perform public.refresh_order_totals(v_order_id);
  return coalesce(new, old);
end;
$$;

