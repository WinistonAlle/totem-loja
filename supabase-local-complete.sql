create or replace function public.customer_orders_with_items(p_customer_document text)
returns table (
  id uuid,
  order_number text,
  total_items integer,
  total_value numeric,
  status text,
  created_at timestamptz,
  order_items jsonb
)
language sql
security definer
set search_path = public
as $$
  with filtered_orders as (
    select o.*
    from public.orders o
    where public.only_digits(coalesce(o.customer_document, '')) = public.only_digits(p_customer_document)
       or coalesce(o.customer_document, '') = coalesce(p_customer_document, '')
    order by o.created_at desc
  )
  select
    o.id,
    o.order_number,
    o.total_items,
    o.total_value,
    o.status,
    o.created_at,
    coalesce(
      (
        select jsonb_agg(
          jsonb_build_object(
            'id', oi.id,
            'product_id', oi.product_id,
            'product_name', oi.product_name,
            'quantity', oi.quantity,
            'unit_price', oi.unit_price,
            'total_price', oi.total_price
          )
          order by oi.created_at asc
        )
        from public.order_items oi
        where oi.order_id = o.id
      ),
      '[]'::jsonb
    ) as order_items
  from filtered_orders o;
$$;

create or replace function public.get_top_selling_products(limit_count integer default 10)
returns table (
  product_id uuid,
  total_quantity numeric,
  total_value numeric
)
language sql
security definer
set search_path = public
as $$
  select
    oi.product_id,
    sum(coalesce(oi.quantity, 0)) as total_quantity,
    sum(coalesce(oi.quantity, 0) * coalesce(oi.unit_price_cents, 0)) / 100.0 as total_value
  from public.order_items oi
  join public.orders o on o.id = oi.order_id
  where oi.product_id is not null
    and coalesce(o.status, '') <> 'cancelado'
  group by oi.product_id
  order by total_quantity desc, total_value desc
  limit greatest(coalesce(limit_count, 10), 1);
$$;

