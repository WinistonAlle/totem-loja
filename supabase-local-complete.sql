create or replace function public.get_employee_by_cpf(p_cpf text)
returns table (
  id uuid,
  full_name text,
  cpf text,
  role text
)
language sql
security definer
set search_path = public
as $$
  select e.id, e.full_name, e.cpf, e.role
  from public.employees e
  where public.only_digits(e.cpf) = public.only_digits(p_cpf)
    and e.active = true
  limit 1;
$$;

create or replace function public.link_employee_to_user(p_cpf text)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  v_uid uuid;
  v_employee public.employees%rowtype;
begin
  v_uid := auth.uid();
  if v_uid is null then
    raise exception 'auth.uid() is null';
  end if;

  select *
    into v_employee
    from public.employees
   where public.only_digits(cpf) = public.only_digits(p_cpf)
   limit 1;

  if v_employee.id is null then
    raise exception 'Employee not found for CPF %', p_cpf;
  end if;

  update public.employees
     set user_id = v_uid,
         updated_at = timezone('utc', now())
   where id = v_employee.id;

  insert into public.profiles (user_id, full_name, role, active)
  values (
    v_uid,
    v_employee.full_name,
    case when lower(coalesce(v_employee.role, 'employee')) = 'admin' then 'admin' else 'employee' end,
    true
  )
  on conflict (user_id) do update
    set full_name = excluded.full_name,
        role = excluded.role,
        active = excluded.active,
        updated_at = timezone('utc', now());
end;
$$;

